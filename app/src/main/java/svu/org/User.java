package svu.org;

public class User {

    String id;
    String name;
    String empNumber;


    public User(String id, String name, String empNumber) {
        this.id = id;
        this.name = name;
        this.empNumber = empNumber;
    }

    public String getId() {

        return id;
    }

    public String toString() {

        return name;
    }
}
